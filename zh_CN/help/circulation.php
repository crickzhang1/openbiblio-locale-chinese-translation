<a name="barc">带有和没有条形码标识签著作的流通</a>：
<br>
OpenBiblio的流通表格设计使在利用条形码扫描器结合印在会员卡上和副本标识签上的<a href="../shared/help.php?page=barcodes">条形码</a>时，能达到著作高效流通。

<br>
当条形码未知时也可以满足著作流通：可以利用”姓“来搜索会员；利用基于联机公共检索目录的<a href="../shared/help.php?page=opacLookup">条形码查询</a>检索副本的条形码来进行借书、还书和借阅请求。