<h1>了解条形码：</h1>
<br><br>

帮助小节：
<ul>
  <li><a href="#memb">会员条形码</a></li>
  <li><a href="#p_id">打印会员ID卡</a></li>
  <li><a href="#copy">著作副本条形码</a></li>
  <li><a href="#p_ba">打印著作副本条形码</a></li>
  <li><a href="#scan">使用条形码扫描仪器</a></li>
</ul>
<br><br>

<a name="memb">会员条形码使用高位补0的数字形式，所有条形码位数相同。</a>
<br><br><br>

<a name="p_id">按照以下说明打印会员卡</a>
<ul>
  <li>访问“报告"功能区，运行“会员搜索报告”。</li>
  <li>通过从左侧导航区选择”会员卡样式设计“子菜单，将报告结果转换成会员卡。</li>
</ul>
<br>

<a name="copy">著作副本的条形码可以使用以下数字形式：</a>
<ul>
  <li>外部</li>
  <li>内部，通过使用<a href="../shared/help.php?page=biblioCopyEdit#auto">自动生成</a>选项</li>
</ul>
<br>

<a name="p_ba">按如下说明打印著作条形码标识签：</a>
<ul>
  <li>访问“报告"功能区，运行“副本搜索报告”。</li>
  <li>通过从左侧导航区选择”标识签样式“子菜单，将报告结果转换成条形码标识签。</li>
</ul>
<br>

<a name="scan">OpenBiblio支持任意的能够模拟键盘的条形码扫描器。</a>包括USB扫描器和通过keyboard wedge接口（一种键盘转接线）连接于电脑和键盘之间的扫描仪。
<br>
大多数扫描器可以编程为在每次扫描时自动附加一个回车（就像在键盘上回车键一样）。这样，OpenBiblio将能够在你一扫描条码后就立即处理请求。